package helpfullThings;

public class Point {
		private int x,y;
		
		// constructors
		public Point(){
			
		}
		public Point(int x,int y){
			this.x=x;
			this.y=y;
		}
		
		//getters and setters
		public int getX() {
			return x;
		}
		public void setX(int x) {
			this.x = x;
		}
		public int getY() {
			return y;
		}
		public void setY(int y) {
			this.y = y;
		}
	
	
}
